public class Insertion_Sort{
    public static void main(String[] args){
        int[] arr = new int[] {3, 5, 15, 6, 2, 4, 62, 3, 7};
        // Dsplay elements in array
        int length = arr.length;
        for (int ind = 0; ind < length; ind++){
            System.out.println(arr[ind]);
        }
    }
}